func main() {
	// ----------------------------------------------------------------
	// 1. Establish a connection to an Ethereum node via WebSockets.
	//    In this example, we're connecting to Infura Mainnet via WS.
	//    Replace <YOUR_INFURA_PROJECT_ID> with your actual project ID.
	// ----------------------------------------------------------------
	wsURL := "wss://eth-mainnet.g.alchemy.com/v2/WceKkUlLDIQLHL5i_jR4FkUzSF4Q7iaa"
	client, err := ethclient.Dial(wsURL)
	if err != nil {
		log.Fatalf("Failed to connect to Ethereum node: %v", err)
	}
	defer client.Close()
	fmt.Println("Successfully connected to Ethereum node via WebSocket.")

	// ----------------------------------------------------------------
	// 2. Parse the minimal ABI so we can unpack the Sync event data.
	// ----------------------------------------------------------------
	contractAbi, err := abi.JSON(strings.NewReader(uniswapPairABI))
	if err != nil {
		log.Fatalf("Failed to parse contract ABI: %v", err)
	}

	// ----------------------------------------------------------------
	// 3. We want to filter for the Sync event of ONE specific Uniswap V2 pool:
	//    0xa2107fa5b38d9bbd2c461d6edf11b11a50f6b974
	//    The Sync event signature in Keccak-256 is:
	//      0x1c411e9a...
	//    which we can get from the contractAbi.Events["Sync"].ID
	// ----------------------------------------------------------------
	poolAddress := common.HexToAddress("0xb4e16d0168e52d35cacd2c6185b44281ec28c9dc")
	syncEventSignature := contractAbi.Events["Sync"].ID

	// Create a filter query to look for logs from this specific address
	// that match the Sync event signature in the first topic.
	query := ethereum.FilterQuery{
		Addresses: []common.Address{poolAddress},
		Topics:    [][]common.Hash{{syncEventSignature}},
	}

	// ----------------------------------------------------------------
	// 4. Subscribe to logs using the filter query. We'll receive logs
	//    in real-time for every new block that includes a Sync event
	//    for this specific Uniswap V2 pair.
	// ----------------------------------------------------------------
	logsChan := make(chan types.Log)
	sub, err := client.SubscribeFilterLogs(context.Background(), query, logsChan)
	if err != nil {
		log.Fatalf("Failed to subscribe to filter logs: %v", err)
	}
	fmt.Println("Subscribed to Sync events for the Uniswap V2 pair. Listening...")

	// ----------------------------------------------------------------
	// 5. Continuously listen for new logs. Whenever a log is received:
	//    - Parse the event data to get reserve0 and reserve1.
	//    - Fetch transaction details using the transaction hash.
	// ----------------------------------------------------------------
	for {
		select {
		case err := <-sub.Err():
			log.Fatalf("Subscription error: %v", err)
		case vLog := <-logsChan:
			// vLog now contains information about the log and the transaction/block
			// in which it was included.

			fmt.Println("------------------------------------------------------")
			fmt.Printf("New Sync event detected in Tx: %s\n", vLog.TxHash.Hex())

			// 5a. Unpack the log data using our contract ABI.
			//     The Sync event has two parameters: reserve0, reserve1
			//     (both are non-indexed uint112).
			syncData, err := contractAbi.Unpack("Sync", vLog.Data)
			if err != nil {
				log.Printf("Failed to unpack sync data: %v\n", err)
				continue
			}

			// The syncData should contain two big.Int values for reserve0 and reserve1.
			reserve0 := syncData[0].(*big.Int) // If the ABI is strictly typed, might be *big.Int or uint64
			reserve1 := syncData[1].(*big.Int)

			fmt.Printf("Parsed Sync Event Reserves => Reserve0: %d, Reserve1: %d\n", reserve0, reserve1)

			// 5b. Retrieve the transaction details from the Ethereum client.
			tx, isPending, err := client.TransactionByHash(context.Background(), vLog.TxHash)
			if err != nil {
				log.Printf("Failed to fetch transaction: %v\n", err)
				continue
			}
			txData, err := client.TransactionReceipt(context.Background(), vLog.TxHash)
			if err != nil {
				log.Printf("Failed to fetch transaction receipt: %v\n", err)
				continue
			}

			fmt.Printf("Transaction details:\n")
			fmt.Printf("  Tx Hash:      %s\n", vLog.TxHash.Hex())
			fmt.Printf("  Block Number: %d\n", txData.BlockNumber.Uint64())
			// fmt.Printf("  From:         %s\n", tx.From().Hex())
			if tx.To() != nil {
				fmt.Printf("  To:           %s\n", tx.To().Hex())
			} else {
				fmt.Println("  To:           Contract creation transaction")
			}
			fmt.Printf("  Value (wei):  %s\n", tx.Value().String())
			fmt.Printf("  Is Pending?:  %t\n", isPending)
			fmt.Println("------------------------------------------------------")
		}
	}
}
